package Domain.ServicioInformacion.Endpoints.EquipoStat;

public class Fixtures {

  private Played played;
  private Wins wins;
  private Draws draws;
  private Loses loses;

  public Played getPlayed() {
    return played;
  }

  public void setPlayed(Played played) {
    this.played = played;
  }

  public Wins getWins() {
    return wins;
  }

  public void setWins(Wins wins) {
    this.wins = wins;
  }

  public Draws getDraws() {
    return draws;
  }

  public void setDraws(Draws draws) {
    this.draws = draws;
  }

  public Loses getLoses() {
    return loses;
  }

  public void setLoses(Loses loses) {
    this.loses = loses;
  }

}
